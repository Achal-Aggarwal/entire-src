MOV AX,0500H
MOV DS,AX
XOR DX,DX;CLEAR DX
MOV BX,0500H;BASE
;;;;;;;;;;;;;
COUNT: 
MOV AL,[BX];
CMP AL,00FFH;
JE END;
INC DX;INC COUNT
INC BX;FETCH NEXT
JMP COUNT;
END:
NOP ;OUT OF LOOP
;;;;;;;;;;;;;
OLOOP:
MOV BX,0500H;
DEC DX
JZ END_SORT
MOV SI,DX;FIRST TIME SETUP
BSORT:
MOV AL,[BX]
MOV CL,[BX]+1
CMP AL,CL
JB LOOPC
SWAP : 
MOV [BX]+1,AL
MOV [BX],CL
LOOPC:
INC BX
DEC SI
JNZ BSORT;INNER LOOP
JMP OLOOP;
END_SORT:
NOP
ret




